#include <stdio.h>
#include <stdlib.h>
// C program that adds two numbers together
// Given two integers, the task is to add these integer numbers and return their sum.

int main(int argc, char const *argv[])
{
    int number1 = 1;
    int number2 = 100;
    printf("%d added with %d is: %d", number1, number2, number1+number2);
    return 0;
}
